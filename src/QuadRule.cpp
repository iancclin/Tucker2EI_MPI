Tucker2EI::QuadRule::QuadRule(Tucker2EI::QuadPoints quad_points)
  : number_quad_points_(quad_points)
  , quad_points_(number_quad_points_, 0)
  , quad_weights_(number_quad_points_, 0)
{
  GetQuadAndWeight(quad_points, quad_points_, quad_weights_);
}

void
Tucker2EI::QuadRule::ChangeQuadPoints(Tucker2EI::QuadPoints quad_points)
{
  number_quad_points_ = quad_points;
  quad_points_.assign(number_quad_points_, 0.0);
  quad_weights_.assign(number_quad_points_, 0.0);
  GetQuadAndWeight(quad_points, quad_points_, quad_weights_);
}

unsigned int
Tucker2EI::QuadRule::GetNumberQuadPoints() const
{
  return number_quad_points_;
}

const std::vector<double> &
Tucker2EI::QuadRule::GetQuadPoints() const
{
  return quad_points_;
}

const std::vector<double> &
Tucker2EI::QuadRule::GetQuadWeights() const
{
  return quad_weights_;
}

